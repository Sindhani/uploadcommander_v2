    public function edit($id)
    {
        $settings = Settings::find($id);
        return view('admin.settings.edit',compact('settings'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $input = $request->all();

        if($request->input('cashout_day_1')) {
            $input['cashout_day_1'] = date('Y-m-d',strtotime($request->input('cashout_day_1')));
        }
        if($request->input('cashout_day_2')) {
            $input['cashout_day_2'] = date('Y-m-d',strtotime($request->input('cashout_day_2')));
        }

        $settings = Settings::find($id);
        $settings->update($input);

        return redirect()->to('admin/settings/'.$id.'/edit')
            ->with('success','Settings updated successfully');
    }

